import os
import json

USER_RECORDS_FILE = "user/data/user_records.json"
DEFAULT_ALLOWED_IPS = "10.66.66.0/24"  # –ó–Ω–∞—á–µ–Ω–∏–µ –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –¥–ª—è —Ä–∞–∑—Ä–µ—à—ë–Ω–Ω—ã—Ö IP


def ensure_directory_exists(filepath):
    """–£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—è –¥–ª—è —Ñ–∞–π–ª–∞ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç."""
    directory = os.path.dirname(filepath)
    if not os.path.exists(directory):
        os.makedirs(directory)


def load_user_records():
    """–ó–∞–≥—Ä—É–∑–∫–∞ –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –∏–∑ JSON."""
    if os.path.exists(USER_RECORDS_FILE):
        with open(USER_RECORDS_FILE, "r") as file:
            return json.load(file)
    return {}


def save_user_records(user_records):
    """–°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –≤ JSON."""
    ensure_directory_exists(USER_RECORDS_FILE)
    with open(USER_RECORDS_FILE, "w") as file:
        json.dump(user_records, file, indent=4)


def create_user():
    """–°–æ–∑–¥–∞–Ω–∏–µ –Ω–æ–≤–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è."""
    username = input("–í–≤–µ–¥–∏—Ç–µ –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è: ").strip()
    if not username:
        print("‚ùå –ò–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –ø—É—Å—Ç—ã–º.")
        return

    allowed_ips = input("–í–≤–µ–¥–∏—Ç–µ —Ä–∞–∑—Ä–µ—à—ë–Ω–Ω—ã–µ IP (–Ω–∞–ø—Ä–∏–º–µ—Ä, 10.66.66.5): ").strip() or DEFAULT_ALLOWED_IPS

    records = load_user_records()
    if username in records:
        print("‚ùå –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å —Ç–∞–∫–∏–º –∏–º–µ–Ω–µ–º —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç.")
        return

    records[username] = {
        "username": username,
        "allowed_ips": allowed_ips,
        "status": "inactive",
    }
    save_user_records(records)
    print(f"‚úÖ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {username} —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω —Å —Ä–∞–∑—Ä–µ—à—ë–Ω–Ω—ã–º–∏ IP: {allowed_ips}")


