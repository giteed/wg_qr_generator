    parser.add_argument("--memory_required", "--mr", type=int, help="–£–∫–∞–∑–∞—Ç—å –º–∏–Ω–∏–º–∞–ª—å–Ω—ã–π –æ–±—ä–µ–º swap –≤ MB.")
