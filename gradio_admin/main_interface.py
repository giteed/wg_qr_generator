from gradio_admin.wg_users_stats import load_data  # –ò–º–ø–æ—Ä—Ç —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π


# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è —Ç–∞–±–ª–∏—Ü—ã
def update_table(show_inactive):
    """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –¥–∞–Ω–Ω—ã–µ —Ç–∞–±–ª–∏—Ü—ã –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å –Ω–æ–≤–æ–π —Å—Ç—Ä—É–∫—Ç—É—Ä–æ–π."""
    table = load_data(show_inactive)
    formatted_table = []

    for row in table:
        user = row[0]
        allowed_ips = row[2]
        recent = row[5]
        endpoints = row[1] or "N/A"
        up = row[4]
        down = row[3]
        state = row[6]

        # –≠–º–æ–¥–∑–∏ –¥–ª—è Recent
        recent_emoji = "üü¢" if state == "active" else "üî¥"

        # –≠–º–æ–¥–∑–∏ –¥–ª—è State
        state_emoji = "‚úÖ" if state == "active" else "‚ùå"

        # –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Å—Ç—Ä–æ–∫ –≤ –ø–µ—Ä–≤–æ–º –∏ –≤—Ç–æ—Ä–æ–º —Å—Ç–æ–ª–±—Ü–µ
        first_col = f"{user}\n{allowed_ips} {recent_emoji}\n{endpoints}"
        second_col = f"Up: {up}\nDown: {down}\nState: {state_emoji}"

        formatted_table.append([first_col, second_col])

    return formatted_table


# –û—Å–Ω–æ–≤–Ω–æ–π –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å
with gr.Blocks(css="style.css") as admin_interface:
    # –ò–∑–º–µ–Ω–µ–Ω–∏–µ –∑–∞–≥–æ–ª–æ–≤–∫–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
    with gr.Row():
        gr.Markdown("## –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞")

    # –ü–æ–ª—è –ø–æ–∏—Å–∫–∞ –∏ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è
    with gr.Row():
        search_input = gr.Textbox(label="–ü–æ–∏—Å–∫", placeholder="–í–≤–µ–¥–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ –¥–ª—è —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏...")
        refresh_button = gr.Button("–û–±–Ω–æ–≤–∏—Ç—å")
        show_inactive = gr.Checkbox(label="–ü–æ–∫–∞–∑–∞—Ç—å –Ω–µ–∞–∫—Ç–∏–≤–Ω—ã—Ö", value=True)

    # –¢–∞–±–ª–∏—Ü–∞ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏
    with gr.Row():
        stats_table = gr.Dataframe(
            headers=["User/IPs", "Up/Down"],
            value=update_table(True),
            interactive=False,
            wrap=True
        )

        def search_and_update_table(query, show_inactive):
            """–§–∏–ª—å—Ç—Ä—É–µ—Ç –¥–∞–Ω–Ω—ã–µ —Ç–∞–±–ª–∏—Ü—ã –ø–æ –∑–∞–ø—Ä–æ—Å—É."""
            table = update_table(show_inactive)
            if query:
                table = [row for row in table if query.lower() in " ".join(map(str, row)).lower()]
            return table

        search_input.change(
            fn=search_and_update_table,
            inputs=[search_input, show_inactive],
            outputs=[stats_table]
        )

        refresh_button.click(
            fn=update_table,
            inputs=[show_inactive],
            outputs=[stats_table]
        )

# –ó–∞–ø—É—Å–∫ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞
